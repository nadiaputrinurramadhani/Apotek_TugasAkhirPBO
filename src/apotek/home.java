    private void selectData() 
    {
        String kolom [] = {"ID_Obat","NamaObat","Keluhan","JenisObat","KategoriObat","EfekSamping","Dosis","Harga","NoKasir","ExpiredDate"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM `tb_obat`";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()) {
                String ID_Obat = rs.getString(1);
                String NamaObat = rs.getString(2);
                String Keluhan = rs.getString(3);
                String JenisObat = "";
                if(rs.getString(3).equals("Sirup")){
                    JenisObat = "Sirup";
                }else if(rs.getString(3).equals("Tablet")){
                    JenisObat = "Tablet";
                }else if(rs.getString(3).equals("Kapsul")){
                    JenisObat = "Kapsul";
                }else if(rs.getString(3).equals("Puyer")){
                    JenisObat = "Puyer";
                }
                String KategoriObat = "";
                if(rs.getString(3).equals("Bayi (0-3 tahun)")){
                    KategoriObat = "Bayi (0-3 tahun)";
                }else if(rs.getString(3).equals("Balita (3-5 tahun")){
                    KategoriObat = "Balita (3-5 tahun";
                }else if(rs.getString(3).equals("Anak (6-12 tahun)")){
                    KategoriObat = "Anak (6-12 tahun)";
                }else if(rs.getString(3).equals("Remaja (12-18 tahun)")){
                    KategoriObat = "Remaja (12-18 tahun))";
                }else if(rs.getString(3).equals("Dewasa (18-50 tahun)")){
                    KategoriObat = "Dewasa (18-50 tahun)";
                }else if(rs.getString(3).equals("Lansia (>50 tahun)")){
                    KategoriObat = "Lansia (>50 tahun)";
                }
                String EfekSamping = rs.getString(4);
                String Dosis = "";
                if(rs.getString(3).equals("1x1")){
                    Dosis = "1x1";
                }else if(rs.getString(3).equals("2x1")){
                    Dosis = "2x1";
                }else if(rs.getString(3).equals("3x1")){
                    Dosis = "3x1";
                }
                String Harga = rs.getString(5);
                String NoKasir = rs.getString(6);                
                String ExpiredDate = rs.getString(7);
                
                String data[] = {ID_Obat,NamaObat,Keluhan,JenisObat,KategoriObat,EfekSamping,Dosis,Harga,NoKasir,ExpiredDate};
                dtm.addRow(data);
            }
                }catch (SQLException ex) {
                    Logger.getLogger(home.class.getName()).log(Level.SEVERE, null, ex);
                }
        Table.setModel(dtm);   
            }
