    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = dateFormat.format(Tanggal.getDate());
        
        String JenisObat = "";
            if(cb_jenis.getSelectedItem().equals("Sirup")) {
                JenisObat = "Sirup";
            }else if(cb_jenis.getSelectedItem().equals("Tablet")) {
                JenisObat = "Tablet";
            }else if(cb_jenis.getSelectedItem().equals("Kapsul")) {
                JenisObat = "Kapsul";
            }else {
                JenisObat = "Puyer";
            }
        
            
               
            String KategoriObat = "";
            if(cb_kategori.getSelectedItem().equals("Bayi (0-3 tahun)")) {
                KategoriObat = "Bayi (0-3 tahun)";
            }else if(cb_kategori.getSelectedItem().equals("Balita (3-5 tahun)")) {
                KategoriObat = "Balita (3-5 tahun)";
            }else if(cb_kategori.getSelectedItem().equals("Anak (6-12 tahun)")) {
                KategoriObat = "Anak (6-12 tahun)";
            }else if(cb_kategori.getSelectedItem().equals("Remaja (12-18 tahun)")) {
                KategoriObat = "Remaja (12-18 tahun)";
            }else if(cb_kategori.getSelectedItem().equals("Dewasa (18-50 tahun)")) {
                KategoriObat = "Dewasa (18-50 tahun)";
            }else {
                KategoriObat = "Lansia (>50 tahun)";
            }
            
            String Dosis = "";
            if(cb_dosis.getSelectedItem().equals("1x1")) {
                Dosis = "1x1";
            }else if(cb_dosis.getSelectedItem().equals("2x1")) {
                Dosis = "2x1";
            }else {
                Dosis = "3x1";
            }
         
            
          if ("".equals(txtid_obat.getText()) || "".equals(txtnama_obat.getText()) ||
                "".equals(txtkeluhan.getText()) || "".equals(JenisObat) ||
                "".equals(KategoriObat) ||
                "".equals(txtefek_samping.getText()) || "".equals(Dosis) ||
                "".equals(txtharga.getText()) || "".equals(txtno_kasir.getText()) ||
                "".equals(Tanggal)) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi data", "Error", JOptionPane.WARNING_MESSAGE);
        }else {
            
            String SQL = "INSERT INTO `tb_obat`(`ID_Obat`,`NamaObat`,`Keluhan`,`JenisObat`,`KategoriObat`,`EfekSamping`,`Dosis`,`Harga`,`NoKasir`,`ExpiredDate`) " + "VALUES('"+txtid_obat.getText()+"','"+txtnama_obat.getText()+"','"+txtkeluhan.getText()+"','"+JenisObat+"','"+KategoriObat+"','"+txtefek_samping.getText()+"','"+Dosis+"','"+txtharga.getText()+"','"+txtno_kasir.getText()+"','"+Tanggal+"')";
            
            int status = KoneksiDB.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "GAGAL", JOptionPane.WARNING_MESSAGE);
            }   
        }
    }//GEN-LAST:event_btn_saveActionPerformed

